var currentURL = window.location.href;
const partnerData = currentURL.includes('-ar') ? partnerDataAr : partnerDataEn;

function displayPartners() {
    const partnerList = document.getElementById('partners-list');
    console.log(partnerList);
    partnerList.innerHTML = '';

    partnerData.forEach(partner => {
        const li = document.createElement('li');
        li.classList.add('partner-item', 'col-md-6', 'col-lg-3', 'mb-4');
        li.innerHTML = `
        <article>
            <div class="partner-img d-flex align-items-center justify-content-center w-100 mb-3">
                <img src="${partner.imgUrl}" alt="${partner.altText}" class="w-100 h-100">
            </div>
            <div class="d-flex justify-content-between flex-column gap-2 text-md-start text-center">
                <div>
                    <h2 class="fs-5-2 lh-sm text-dark partner-title">
                        ${partner.title}
                    </h2>
                    <p class="fs-6-1 ls-1 text-black-50 mt-2 fw-lighter partner-description mb-0">
                        ${partner.description}
                    </p>
                </div>
                <!-- سيتم إخفاء الزر إذا كان النص صغيرًا -->
                <div class="d-flex justify-content-end w-100">
                    <button class="read-more-btn" style="display: ${partner.description.length > 100 ? 'block' : 'none'};">
                        <svg viewBox="0 0 24 24" width="16" fill="none" xmlns="http://www.w3.org/2000/svg">
                            <path transform="rotate(180 12 12)" d="M18.2929 15.2893C18.6834 14.8988 18.6834 14.2656 18.2929 13.8751L13.4007 8.98766C12.6195 8.20726 11.3537 8.20757 10.5729 8.98835L5.68257 13.8787C5.29205 14.2692 5.29205 14.9024 5.68257 15.2929C6.0731 15.6835 6.70626 15.6835 7.09679 15.2929L11.2824 11.1073C11.673 10.7168 12.3061 10.7168 12.6966 11.1073L16.8787 15.2893C17.2692 15.6798 17.9024 15.6798 18.2929 15.2893Z" fill="#0F0F0F"></path>
                        </svg>
                        ${currentURL.includes('-ar') ? 'اقرأ المزيد' : 'Read more'}
                    </button>
                </div>
            </div>
        </article>
        `;
    
        const button = li.querySelector('.read-more-btn');
        const description = li.querySelector('.partner-description');
    
        button.addEventListener('click', () => {
            const isExpanded = description.classList.toggle('expanded');
            if(isExpanded){
                button.classList.add('expanded');
            }
            button.innerHTML = `
                <svg viewBox="0 0 24 24" width="16" fill="none" xmlns="http://www.w3.org/2000/svg">
                    <path transform="${isExpanded ? 'rotate(0 12 12)' : 'rotate(180 12 12)'}" 
                        d="M18.2929 15.2893C18.6834 14.8988 18.6834 14.2656 18.2929 13.8751L13.4007 8.98766C12.6195 8.20726 11.3537 8.20757 10.5729 8.98835L5.68257 13.8787C5.29205 14.2692 5.29205 14.9024 5.68257 15.2929C6.0731 15.6835 6.70626 15.6835 7.09679 15.2929L11.2824 11.1073C11.673 10.7168 12.3061 10.7168 12.6966 11.1073L16.8787 15.2893C17.2692 15.6798 17.9024 15.6798 18.2929 15.2893Z" fill="#0F0F0F"></path>
                </svg>
                ${currentURL.includes('-ar') 
                    ? (isExpanded ? 'إقرأ أقل' : 'اقرأ المزيد') 
                    : (isExpanded ? 'Read less' : 'Read more')}
            `;
        });
    
        partnerList.appendChild(li);
    });
}


function init() {
    displayPartners();
}
init();